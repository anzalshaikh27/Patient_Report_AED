package model;

/**
 *
 * @author anzal
 */
public class Doctor {
    
    private String doc_name;
    private String office_addr;
    private String doc_id;
    private String specialty;
    private String visit_fee;
    private String visit_time;
    private String doc_number;

    public String getDoc_name() {
        return doc_name;
    }

    public void setDoc_name(String doc_name) {
        this.doc_name = doc_name;
    }

    public String getOffice_addr() {
        return office_addr;
    }

    public void setOffice_addr(String office_addr) {
        this.office_addr = office_addr;
    }

    public String getDoc_id() {
        return doc_id;
    }

    public void setDoc_id(String doc_id) {
        this.doc_id = doc_id;
    }

    public String getSpecialty() {
        return specialty;
    }

    public void setSpecialty(String specialty) {
        this.specialty = specialty;
    }

    public String getVisit_fee() {
        return visit_fee;
    }

    public void setVisit_fee(String visit_fee) {
        this.visit_fee = visit_fee;
    }

    public String getVisit_time() {
        return visit_time;
    }

    public void setVisit_time(String visit_time) {
        this.visit_time = visit_time;
    }

    public String getDoc_number() {
        return doc_number;
    }

    public void setDoc_number(String doc_number) {
        this.doc_number = doc_number;
    }
    
    
}
